#include "project.h"

int	ft_auto(t_frac *f)
{
	int	i;

	i = 0;
	while (i < 100)
	{
		f->mult += 2;
		f->mask -= 0x0F0000;
		ft_new_mlx(f);
		i++;
	}
	return (1);
}
