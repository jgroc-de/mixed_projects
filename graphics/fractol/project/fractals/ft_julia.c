#include "project.h"

int	ft_julia(t_frac *f, t_coord z)
{
	int		i;

	i = 0;
	while (ft_cnorme(z) < 4 && i < f->n)
	{
		z = ft_cadd(ft_cpow(z, f->d), f->c);
		i++;
	}
	return (i);
}
